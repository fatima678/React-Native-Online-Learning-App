import React from 'react';
import { NavigationContainer } from '@react-navigation/native';
import { createStackNavigator } from '@react-navigation/stack';
import SplashScreen from './components/SplashScreen';
import InitialScreen from './components/InitialScreen';
import RegisterScreen from './components/RegisterScreen';
import LoginScreen from './components/LoginScreen';
import AdminLoginScreen from './components/AdminLoginScreen';
import AdminPanel from './components/AdminPanel';
import ViewUsers from './components/ViewUsers';
import ManageContent from './components/ManageContent';
import ViewCourses from './components/ViewCourses';
import AddCourses from './components/AddCourses.java'; 
import HomeScreen from './components/HomeScreen';
import EditCourse from './components/EditCourse';
import ViewUserCourses from './components/UserViewCourses'
import AddCourseDetails from './components/AddCourseDetails';
import CourseDetail from './components/CourseDetail';
import UserProfile from './components/UserProfile';


const Stack = createStackNavigator();

const App = () => {
  return (
    <NavigationContainer>
      <Stack.Navigator initialRouteName="Splash">
        <Stack.Screen
          name="Splash"
          component={SplashScreen}
          options={{ headerShown: false }}
        />
        <Stack.Screen
          name="Initial"
          component={InitialScreen}
          options={{ headerShown: false }}
        />
        <Stack.Screen
          name="Register"
          component={RegisterScreen}
          options={{ headerShown: false }}
        />
        <Stack.Screen
          name="Login"
          component={LoginScreen}
          options={{ headerShown: false }}
        />
        <Stack.Screen
          name="AdminLogin"
          component={AdminLoginScreen}
        />
        <Stack.Screen
          name="AdminPanel"
          component={AdminPanel}
          options={{ headerShown: false }}
        />
        <Stack.Screen
          name="Home"
          component={HomeScreen}
        />
        <Stack.Screen
          name="ViewCourses"
          component={ViewCourses}
        />
        <Stack.Screen
          name="AddCourses"
          component={AddCourses}
        />
        <Stack.Screen
          name="EditCourse"
          component={EditCourse}
        />
        <Stack.Screen
          name="ViewUsers"
          component={ViewUsers}
        />
        <Stack.Screen
          name="ManageContent"
          component={ManageContent}
        />
        <Stack.Screen
          name="UserViewCourses"
          component={ViewUserCourses}
        />
        <Stack.Screen
          name="AddCourseDetails"
          component={AddCourseDetails}
        />
        <Stack.Screen
          name="CourseDetail"
          component={CourseDetail}
        />
        <Stack.Screen
          name="UserProfile"
          component={UserProfile}
        />
      </Stack.Navigator>
    </NavigationContainer>
  );
};

export default App;
